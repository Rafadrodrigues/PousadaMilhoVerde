    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(Resultado.getSelectedRow() != -1){
            
            DefaultTableModel cadastroCli = (DefaultTableModel) Resultado.getModel();
            cadastroCli.removeRow(Resultado.getSelectedRow());
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um funciónario para excluir.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
           if(Resultado.getSelectedRow() != -1){
                Resultado.setValueAt(nome1.getText(), Resultado.getSelectedRow(), 0);
                Resultado.setValueAt(cpf1.getText(), Resultado.getSelectedRow(), 1);
                Resultado.setValueAt(usuario.getText(), Resultado.getSelectedRow(), 2);
                Resultado.setValueAt(senha.getText(), Resultado.getSelectedRow(), 3);
                Resultado.setValueAt(cargo.getText(), Resultado.getSelectedRow(),4);
                Resultado.setValueAt(salario.getText(), Resultado.getSelectedRow(), 5);
         }
    }//GEN-LAST:event_jButton4ActionPerformed


